# Favorite
# Write a program that asks the user for their favorite food,
# favorite drink, and the name of their favorite person.
# Output a statement that uses all of this information. (Be creative!)

# Get user inputs and store as variables
favorite_food = input("What is your favorite food? ")
favorite_drink = input("What is your favorite drink? ")
favorite_person = input("Who is your favorite person? ")

# Display output
print(favorite_person + " bought you " + favorite_food + " and " + favorite_drink + ".")
